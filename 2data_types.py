# Variable name defining rules
# Can not start with a number
# Case sensitive (name != Name)
# Can not have spaces

# Assign value 10 to the variable "number"
number = 10
print(number)

# Change value of the variable "number"
number = 20
print(number)

# Add 30 to value of the variable "number"
number += 30
print(number)

